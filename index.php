                  young children and those who support a spouse or a disabled adult or child. In addition to helping to
                  support dependents, life insurance can help provide immediate cash at death. Insurance proceeds are a
                  handy source of cash to pay the deceased's debts, funeral expenses, and income or estate taxes.</p>
