                <h2 style="font-size: 120%; color: dodgerblue">Will There Always Be Closing Costs?</h2>
                <p>Yes, there are always closing costs. Whether you're the buyer or the seller or even refinancing,
                    you're going to have closing costs. If you're the buyer, your mortgage closing costs account for
                    about 2%-5% of the purchase price. If you're the seller, about 6% of the sales price will go toward
                    paying realtor fees.</p>
                <h2 style="font-size: 120%; color: dodgerblue">Can I Roll Closing Costs into the Loan?</h2>
                <p>Typically, the only loan that allows you to include the closing costs into the mortgage is a USDA
                    loan.</p>
                <p>Home loans have a loan-to-value ratio (LTV ratios) that puts a cap on how much you can include in the
                    loan. This is calculated by taking your loan amount plus the closing costs and then subtracting the
                    down payment. The number you're left with must not exceed the LTV limit for that specific loan.</p>
                <p>The only exception to the LTV ratio rule are USDA loans. You can include your closing costs into a
                    USDA loan just as long as your property appraises for more than the sales price.</p>
